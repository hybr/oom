    public static function getTeamTree($departmentId = null) {
        $allTeams = $departmentId ? static::getTeamsByDepartment($departmentId) : static::all();
        $tree = [];
        $teamMap = [];

        // Create a map for quick lookup
        foreach ($allTeams as $team) {
            $teamMap[$team->id] = $team;
            $team->children = [];
        }

        // Build the tree structure
        foreach ($allTeams as $team) {
            if ($team->parent_team_id && isset($teamMap[$team->parent_team_id])) {
                $teamMap[$team->parent_team_id]->children[] = $team;
            } else {
                $tree[] = $team;
            }
        }

        return $tree;
    }

