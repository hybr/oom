    public function validate() {
        $errors = [];

        if (empty($this->attributes['name'])) {
            $errors[] = "Team name is required";
        }

        if (empty($this->attributes['popular_organization_department_id'])) {
            $errors[] = "Department is required";
        }

        if (empty($this->attributes['team_type'])) {
            $errors[] = "Team type is required";
        }

        if (empty($this->attributes['operational_status'])) {
            $errors[] = "Operational status is required";
        }

        // Validate team type
        $validTypes = ['Functional', 'Cross-functional', 'Project', 'Product', 'Scrum', 'DevOps', 'Support', 'Research'];
        if (!in_array($this->attributes['team_type'], $validTypes)) {
            $errors[] = "Invalid team type";
        }

        // Validate operational status
        $validStatuses = ['Active', 'Inactive', 'Suspended', 'Dissolved', 'Pending', 'Planning', 'Forming', 'Storming', 'Norming', 'Performing'];
        if (!in_array($this->attributes['operational_status'], $validStatuses)) {
            $errors[] = "Invalid operational status";
        }

        // Validate team size constraints
        if (isset($this->attributes['current_size']) && isset($this->attributes['max_size'])) {
            if ($this->attributes['current_size'] > $this->attributes['max_size']) {
                $errors[] = "Current team size cannot exceed maximum size";
            }
        }

        if (isset($this->attributes['current_size']) && isset($this->attributes['min_size'])) {
            if ($this->attributes['current_size'] < $this->attributes['min_size']) {
                $errors[] = "Current team size cannot be below minimum size";
            }
        }

        return $errors;
    }

    public function beforeSave() {
        // Set default values
        if (empty($this->attributes['operational_status'])) {
            $this->attributes['operational_status'] = 'Active';
        }

        if (empty($this->attributes['is_active'])) {
            $this->attributes['is_active'] = 1;
        }

        if (empty($this->attributes['status'])) {
            $this->attributes['status'] = $this->attributes['operational_status'] ?? 'Active';
        }

        if (empty($this->attributes['budget_currency'])) {
            $this->attributes['budget_currency'] = 'USD';
        }

        if (empty($this->attributes['priority_level'])) {
            $this->attributes['priority_level'] = 'Medium';
        }

        if (empty($this->attributes['current_size'])) {
            $this->attributes['current_size'] = 0;
        }

        if (empty($this->attributes['min_size'])) {
            $this->attributes['min_size'] = 1;
        }

        // Generate slug from name if not provided
        if (empty($this->attributes['slug']) && !empty($this->attributes['name'])) {
            $this->attributes['slug'] = strtolower(str_replace(' ', '-', $this->attributes['name']));
        }

        // Set timestamps
        if (empty($this->attributes['created_at'])) {
            $this->attributes['created_at'] = date('Y-m-d H:i:s');
        }
        $this->attributes['updated_at'] = date('Y-m-d H:i:s');

        return true;
    }

